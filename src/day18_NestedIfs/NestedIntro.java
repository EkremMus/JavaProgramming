package day18_NestedIfs;

public class NestedIntro {

    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");
        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");
        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");
        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Wooden Spoon");
        }

        System.out.println("-----------------------");

        for (int j = 0; j < 4; j++) {

            for (int i = 0; i < 5; i++) {
                System.out.println("Wooden Spoon");

            }
        }

    }
}
